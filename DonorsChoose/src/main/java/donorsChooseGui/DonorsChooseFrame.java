    private void saveMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveMenuItemActionPerformed
        fileChooser.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        fileChooser.setMultiSelectionEnabled(false);
        fileChooser.setDialogTitle("Donors Choose");
        int value = fileChooser.showSaveDialog(DonorsChooseFrame.this);
        if(value == JFileChooser.APPROVE_OPTION){
            File file = fileChooser.getSelectedFile();
            String filePath = file.getAbsolutePath();
            projects.save(filePath);
        }
    }//GEN-LAST:event_saveMenuItemActionPerformed

    private void exitMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exitMenuItemActionPerformed
        int answer = JOptionPane.showConfirmDialog(DonorsChooseFrame.this, "Do you want to save?", "Save"
                , JOptionPane.YES_NO_CANCEL_OPTION);
        switch (answer){
            case JOptionPane.NO_OPTION:
                dispose();
                break;
            case JOptionPane.YES_OPTION:
                saveMenuItem.doClick();
                dispose();
                break;
            default:
                break;
        }
    }//GEN-LAST:event_exitMenuItemActionPerformed

